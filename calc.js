const add = (a, b) => {
  return a + b;
};
const mul = (a, b) => {
  return a * b;
};
const div = (a, b) => {
  return a / b;
};
const sub = (a, b) => {
  return a - b;
};

// for addition
console.log(add(10, 2));

// for multiplication
console.log(mul(10, 2));

// for division
console.log(div(10, 2));

// for subtraction
console.log(sub(10, 2));
